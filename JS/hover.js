const element01 = document.querySelector('.lePre');
const element02 = document.querySelector('.card-lePre');
element01.addEventListener('mouseover', function () {
  element02.classList.remove('visibility');
});
element01.addEventListener('mouseout', function () {
  element02.classList.add('visibility');
});
element01.addEventListener('touchstart', function () {
  element02.classList.remove('visibility');
});
element01.addEventListener('touchend', function () {
  element02.classList.add('visibility');
});

